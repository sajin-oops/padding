#PADDING
print("***{msg}***".format(msg="WELCOME"))
# O/P - ***WELCOME***
print("***{msg:15}***".format(msg="WELCOME"))
#O/P - ***WELCOME        *** add space end of the welcome
print("***{msg:>15}***".format(msg="WELCOME"))
#O/P - ***        WELCOME***
print("***{msg:^15}***".format(msg="WELCOME"))
#O/P -***    WELCOME    ***
print("***{:^15}***".format("WELCOME"))
#O/P -***    WELCOME    ***